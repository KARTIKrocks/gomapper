var binaryPath string

func TestMain(m *testing.M) {
	os.Exit(testMain(m))
}

func testMain(m *testing.M) int {
	// Build the binary once for all integration tests.
	tmp, err := os.MkdirTemp("", "gomapper-test-*")
	if err != nil {
		panic(err)
	}
	defer os.RemoveAll(tmp)

	binaryPath = filepath.Join(tmp, "gomapper")
	build := exec.Command("go", "build", "-o", binaryPath, ".")
	build.Stderr = os.Stderr
	if err := build.Run(); err != nil {
		panic("failed to build gomapper: " + err.Error())
	}

	return m.Run()
}

// runGomapper executes the gomapper binary in the given directory with the given flags.
// It returns stdout, stderr, and any error.
func runGomapper(t *testing.T, dir string, args ...string) (string, string, error) {
	t.Helper()
	cmd := exec.Command(binaryPath, args...)
	cmd.Dir = dir
	var stdout, stderr strings.Builder
	cmd.Stdout = &stdout
	cmd.Stderr = &stderr
	err := cmd.Run()
	return stdout.String(), stderr.String(), err
}

// readGenerated reads the generated file from the given directory.
func readGenerated(t *testing.T, dir, filename string) string {
	t.Helper()
	data, err := os.ReadFile(filepath.Join(dir, filename))
	if err != nil {
		t.Fatalf("reading generated file: %v", err)
	}
	return string(data)
}

// assertCompiles runs `go build` in the given directory to verify generated code compiles.
func assertCompiles(t *testing.T, dir string) {
	t.Helper()
	cmd := exec.Command("go", "build", "./...")
	cmd.Dir = dir
	out, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("generated code does not compile:\n%s", out)
	}
}

// cleanGenerated removes a generated file, ignoring errors.
func cleanGenerated(dir, filename string) {
	os.Remove(filepath.Join(dir, filename))
}

// containsNormalized checks if s contains substr after collapsing runs of whitespace
// to single spaces. This handles gofmt alignment differences.
func containsNormalized(s, substr string) bool {
	normalize := func(in string) string {
		var b strings.Builder
		inSpace := false
		for _, r := range in {
			if r == ' ' || r == '\t' {
				if !inSpace {
					b.WriteByte(' ')
					inSpace = true
				}
			} else {
				b.WriteRune(r)
				inSpace = false
			}
		}
		return b.String()
	}
	return strings.Contains(normalize(s), normalize(substr))
}

func testdataDir(name string) string {
	wd, _ := os.Getwd()
	return filepath.Join(wd, "testdata", name)
}

func TestIntegration_BasicRegisterMode(t *testing.T) {
	dir := testdataDir("basic")
	out := "mapper_gen.go"
	defer cleanGenerated(dir, out)

	_, stderr, err := runGomapper(t, dir, "-src", "User", "-dst", "UserDTO", "-mode", "register", "-output", out)
	if err != nil {
		t.Fatalf("gomapper failed: %s", stderr)
	}

	gen := readGenerated(t, dir, out)

	// Verify key parts of the generated code.
	for _, want := range []string{
		"// Code generated by gomapper; DO NOT EDIT.",
		"package basic",
		`import "github.com/KARTIKrocks/mapper"`,
		"mapper.Register(func(src User) UserDTO {",
		"ID:   src.ID,",
		"Name: src.Name,",
		"Age:  int64(src.Age),",
	} {
		if !strings.Contains(gen, want) {
			t.Errorf("generated code missing %q", want)
		}
	}

	// Email should NOT appear (not in UserDTO).
	if strings.Contains(gen, "Email") {
		t.Error("generated code should not contain Email")
	}

	assertCompiles(t, dir)
}

