func TestGenerate_RegisterMode(t *testing.T) {
	data := Data{
		PkgName: "mypackage",
		Pairs: []PairData{
			{
				SrcType: "User",
				DstType: "UserDTO",
				Mappings: []matcher.FieldMapping{
					{SrcAccessor: "ID", DstField: "ID"},
					{SrcAccessor: "Name", DstField: "Name"},
				},
			},
		},
	}

	out, err := Generate(data, ModeRegister)
	if err != nil {
		t.Fatal(err)
	}
	src := string(out)

	// Check essential parts of the output.
	mustContain(t, src, "// Code generated by gomapper; DO NOT EDIT.")
	mustContain(t, src, "package mypackage")
	mustContain(t, src, `import "github.com/KARTIKrocks/mapper"`)
	mustContain(t, src, "mapper.Register(func(src User) UserDTO {")
	mustContain(t, src, "ID:   src.ID,")
	mustContain(t, src, "Name: src.Name,")
}

