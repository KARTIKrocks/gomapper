func TestGenerate_NilSafeDeref(t *testing.T) {
	for _, mode := range []Mode{ModeRegister, ModeFunc, ModeBoth} {
		t.Run(string(mode), func(t *testing.T) {
			data := Data{
				PkgName: "pkg",
				NilSafe: true,
				Pairs: []PairData{
					{
						SrcType: "A",
						DstType: "B",
						Mappings: []matcher.FieldMapping{
							{SrcAccessor: "Name", DstField: "Name", Deref: true, DstTypeName: "string"},
						},
					},
				},
			}

			out, err := Generate(data, mode)
			if err != nil {
				t.Fatal(err)
			}
			src := string(out)

			// Should have var declaration.
			mustContain(t, src, "var _Name string")
			// Should have nil check.
			mustContain(t, src, "if src.Name != nil {")
			// Should assign inside nil check.
			mustContain(t, src, "_Name = *src.Name")
			// Struct literal should reference the local variable.
			mustContain(t, src, "Name: _Name,")
			// Should NOT have bare *src.Name in the struct literal.
			mustNotContain(t, src, "Name: *src.Name,")
		})
	}
}

