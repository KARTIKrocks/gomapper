func TestGenerate_FuncModeInlineSlice(t *testing.T) {
	data := Data{
		PkgName: "pkg",
		Pairs: []PairData{
			{
				SrcType: "A",
				DstType: "B",
				Mappings: []matcher.FieldMapping{
					{SrcAccessor: "Name", DstField: "Name"},
					{SrcAccessor: "Items", DstField: "Items", IsSliceMap: true, SliceSrc: "Item", SliceDst: "ItemDTO"},
				},
			},
		},
	}

	out, err := Generate(data, ModeFunc)
	if err != nil {
		t.Fatal(err)
	}
	src := string(out)

	// func mode should never import mapper.
	mustNotContain(t, src, `import "github.com/KARTIKrocks/mapper"`)
	mustNotContain(t, src, "mapper.MapSlice")

	// Should generate inline loop.
	mustContain(t, src, "_Items := make([]ItemDTO, len(src.Items))")
	mustContain(t, src, "MapItemToItemDTO(_v)")

	// Struct literal should reference the local variable.
	mustContain(t, src, "Items: _Items,")
}

